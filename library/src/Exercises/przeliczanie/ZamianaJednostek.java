package Exercises.przeliczanie;

public class ZamianaJednostek {

    double mNaCm(double x) {

        return x*100;
    }

    double mNaMm(double x) {

        return x*1000;
    }

    double cmNaM(double x) {

        return x/100;
    }

    double mmNaM(double x){

        return  x/1000;
    }
}
